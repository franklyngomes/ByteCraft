const mongoose = require("mongoose");
const Schema = mongoose.Schema;
const ClientSchema = new Schema(
  {
    title: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    deleted: {
      type: Boolean,
      default: false,
    },
    deletedAt: {
      type: Date,
      default: null,
    },
  },
  { timestamps: true }
);
const ClientModel = mongoose.model("client", ClientSchema);
module.exports = ClientModel;
